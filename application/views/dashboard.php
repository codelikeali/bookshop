<script>
$(document).ready(function() {

    load_data();

    function load_data(query) {
        $.ajax({
            url: "<?php echo base_url(); ?>dashboard_data",
            method: "POST",
            data: {
                query: query
            },
            success: function(data) {
                $('#result').html(data);
            }
        })
    }

    $('#search_text').keyup(function() {
        var search = $(this).val();
        if (search != '') {
            load_data(search);
        } else {
            load_data();
        }
    });


    $('#select_text').on('change', function(event){
       
        var search = $(this).val();
        
        if (search != '') {
            load_data(search);
        } else {
            load_data();
        }
    });
});
</script>